function missingNumber(nums: number[]): number {
    let left = 0,
        right = nums.length - 1
    while (left <= right) {
        const mid = left + Math.floor(right - left >> 1)
        nums[mid] !== mid ? right = mid - 1 : left = mid + 1
    }
    return left
}

